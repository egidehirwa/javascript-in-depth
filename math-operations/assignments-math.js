//  Mathematical assighnements - shortcuts to shorten the amount of code written

// ++ increment by 1

let points = 10;

points++

console.log(points);

// -- decrement by 1

let score = 20;

score--

console.log(score);

// += increment by value

score += 20;
console.log(score);

// -= decrement by value

score -= 19;

console.log(score);

// *= multiply by value

let age = 7;

age *= 2

console.log(age);

// /= divide by value

age /= 2;

console.log(age);

// %= modulus by value

// age %= 7;

console.log(age);

// **= exponentiate by value

age **= 2;
console.log(age);
